fl = True  # –Ω–∞—á–∞–ª–æ —Ü–∏–∫–ª–∞ –∑–∞–Ω–µ—Å–µ–Ω–∏—è —Ñ–æ—Ä–º—É–ª –≤ —Å–ø–∏—Å–æ–∫
i = 0
while fl == True:
    f_list.append(calculate_residue(f_list[i + 1], residue_func(f_list[i], f_list[i + 1])))
