# –û–ø—Ä–µ–¥–µ–ª–∏–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–µ–π—Å—Ç–≤–∏—Ç–µ–ª—å–Ω—ã—Ö –∫–æ—Ä–Ω–µ–π –∫–∞–∫ —Ä–∞–∑–Ω–∏—Ü—É –º–µ–∂–¥—É –Ω–∏–º–∏
inf_table = create_shturm_table(f_list)  # —Ç–∞–±–ª–∏—Ü–∞ –®—Ç—É—Ä–º–∞ –¥–ª—è –ø–ª—é—Å\–º–∏–Ω—É—Å –±–µ—Å–∫–æ–Ω–µ—á–Ω–æ—Å—Ç–∏
valid_roots = abs(count_sign_change(inf_table, 0) - count_sign_change(inf_table, 1))  # –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–µ–π—Å—Ç–≤–∏—Ç–µ–ª—å–Ω—ã—Ö –∫–æ—Ä–Ω–µ–π

# –æ–ø—Ä–µ–¥–µ–ª–∏–º –¥–∏–∞–ø–∞–∑–æ–Ω, –≤ –∫–æ—Ç–æ—Ä–æ–º –±—É–¥–µ–º –∏—Å–∫–∞—Ç—å –∫–æ—Ä–Ω–∏ - –æ—Ç '0' –¥–æ —á–∏—Å–ª–∞ –¥–µ–π—Å—Ç–≤–∏—Ç–µ–ª—å–Ω—ã—Ö –∫–æ—Ä–Ω–µ–π.
# –≤ —ç—Ç–æ–º –¥–∏–∞–ø–∞–∑–æ–Ω–µ –ø–æ–ª—É—á–∞–µ–º –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ [–∞—Ä–≥—É–º–µ–Ω—Ç, –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–æ—Ä–Ω–µ–π —Ñ—É–Ω–∫—Ü–∏–∏] –ø–æ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—é –∞—Ä–≥—É–º–µ–Ω—Ç–∞.
# –í—ã–¥–µ–ª–∏—Ç—å –∏—Å–∫–æ–º—ã–µ –¥–∏–∞–ø–∞–∑–æ–Ω—ã, –≤ –∫–æ—Ç–æ—Ä–æ—Ö –Ω–∞—Ö–æ–¥—è—Ç—Å—è –∫–æ—Ä–Ω–∏ —É—Ä–∞–≤–Ω–µ–Ω–∏—è

max_root_list = []  # —Å–ø–∏—Å–æ–∫ –∫–æ–ª–∏—á–µ—Å—Ç–≤ –∏–∑–º–µ–Ω–µ–Ω–∏–π –∑–Ω–∞–∫–∞ —Å–∏—Å—Ç–µ–º—ã –®—Ç—É—Ä–º–∞ –ø—Ä–∏ —Ä–∞–∑–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏—è—Ö –∞—Ä–≥—É–º–µ–Ω—Ç–∞ 'x'.
max_root_list.append(count_sign_func_change(create_sign_list(f_list, 0)))  # x = 0
arg_max_root_list = [[0, max_root_list[0]]]  # —Å–ø–∏—Å–æ–∫ [–∞—Ä–≥—É–º–µ–Ω—Ç, –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–æ—Ä–Ω–µ–π —Ñ—É–Ω–∫—Ü–∏–∏]
x_arg = 1  # –≤—Ç–æ—Ä–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ —Ö = 1

while (0 not in max_root_list) or (valid_roots not in max_root_list):
    # —Å–æ—Å—Ç–∞–≤–∏–º —Å–ø–∏—Å–æ–∫ [–∞—Ä–≥—É–º–µ–Ω—Ç, –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–æ—Ä–Ω–µ–π —Ñ—É–Ω–∫—Ü–∏–∏] –¥–ª—è —Ä–∞–∑–Ω—ã—Ö –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤
    # –ø–æ–∫–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω—ã –∑–Ω–∞—á–µ–Ω–∏—è –∏–∑–º–µ–Ω–µ–Ω–∏–π —Å–∏—Å—Ç–µ–º—ã —à—Ç—É—Ä–º–∞: '0' –∏ '–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–µ–π—Å—Ç–≤–∏—Ç–µ–ª—å–Ω—ã—Ö –∫–æ—Ä–Ω–µ–π'
    # –Ω–µ –∑–Ω–∞—é –ø–æ—á–µ–º—É, –Ω–æ —Ç—Ä–µ–±—É–µ—Ç—Å—è "–∏–ª–∏" –≤–º–µ—Å—Ç–æ "–∏"
    max_root_list.append(count_sign_func_change(create_sign_list(f_list, x_arg)))
    arg_max_root_list.append([x_arg, count_sign_func_change(create_sign_list(f_list, x_arg))])
    max_root_list.append(count_sign_func_change(create_sign_list(f_list, -x_arg)))
    arg_max_root_list.append([-x_arg, count_sign_func_change(create_sign_list(f_list, -x_arg))])
    x_arg += 1

# –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ [–∞—Ä–≥—É–º–µ–Ω—Ç, –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–æ—Ä–Ω–µ–π —Ñ—É–Ω–∫—Ü–∏–∏] –ø–æ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—é –∞—Ä–≥—É–º–µ–Ω—Ç–∞
arg_max_root_list.sort()

roots_ranges_list = []  # —Å–ø–∏—Å–æ–∫ –¥–∏–∞–ø–∞–∑–æ–Ω–æ–≤ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤, –≤ –∫–æ—Ç–æ—Ä—ã—Ö –ª–µ–∂–∞—Ç –∫–æ—Ä–Ω–∏ —Ñ—É–Ω–∫—Ü–∏–∏
root = arg_max_root_list[0][1]  # –∑–∞–ø–æ–º–∏–Ω–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ "–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–æ—Ä–Ω–µ–π —Ñ—É–Ω–∫—Ü–∏–∏" (–∏–∑–º–µ–Ω–µ–Ω–∏–π –∑–Ω–∞–∫–∞)
for i in range(len(arg_max_root_list)):
    # –æ–ø—Ä–µ–¥–µ–ª—è–µ–º –¥–∏–∞–ø–∞–∑–æ–Ω—ã, –Ω–∞ –∫–æ—Ç–æ—Ä—ã—Ö –ª–µ–∂–∞—Ç –∫–æ—Ä–Ω–∏
    if root != arg_max_root_list[i][1]:  # –ï—Å–ª–∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∏–∑–º–µ–Ω–µ–Ω–∏–π –∑–Ω–∞–∫–∞ —Ñ—É–Ω–∫—Ü–∏–∏ –∏–∑–º–µ–Ω–∏–ª–æ—Å—å
        root = arg_max_root_list[i][1]
        roots_ranges_list.append([arg_max_root_list[i - 1][0], arg_max_root_list[i][0]])  # –¥–∏–∞–ø–∞–∑–æ–Ω

# –≤—ã–≤–æ–¥–∏–º —Ç–∞–±–ª–∏—Ü—É —à—Ç—É—Ä–º–∞
# ***********************************************************************************************************************
print("     –¢–∞–±–ª–∏—Ü–∞ –®—Ç—É—Ä–º–∞:     ")
print(str(' ').rjust(33), end='')
for i in range(len(roots_ranges_list)):  # –≤—ã–≤–æ–¥ –∑–Ω–∞—á–µ–Ω–∏–π –∞—Ä–≥—É–º–µ–Ω—Ç–∞
    print(roots_ranges_list[i][0], end='        ')
    print(roots_ranges_list[i][1], end='        ')
print()  # –ø–µ—Ä–µ–Ω–æ—Å —Å—Ç—Ä–æ–∫–∏

# –≤—ã–≤–æ–¥–∏–º —Ñ—É–Ω–∫—Ü–∏—é –∏ –µ–µ –∑–Ω–∞–∫ –ø—Ä–∏ –∑–∞–¥–∞–Ω–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏—è—Ö –∞—Ä–≥—É–º–µ–Ω—Ç–∞
for j in range(len(f_list)):
    print(str(f_list[j]).rjust(25), end='    ')  # –≤—ã—Ä–∞–≤–Ω–∏–≤–∞–µ–º —Å—Ç–æ–ª–±—Ü—ã
    for i in range(len(roots_ranges_list)):
        print(f"{str(define_sign_func_value(calculation_func_value(f_list[j], roots_ranges_list[i][0]))).rjust(6)}",
              end='    ')
        print(f"{str(define_sign_func_value(calculation_func_value(f_list[j], roots_ranges_list[i][1]))).rjust(6)}",
              end='    ')
    print()

print(str(" ").rjust(33), end='')
# –≤—ã–≤–æ–¥–∏–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∏–∑–º–µ–Ω–µ–Ω–∏–π –∑–Ω–∞–∫–∞ —Ñ—É–Ω–∫—Ü–∏–∏ –ø—Ä–∏ –∑–∞–¥–∞–Ω–Ω–æ–º –∞—Ä–≥—É–º–µ–Ω—Ç–µ
for i in range(len(roots_ranges_list)):
    print(count_sign_func_change(create_sign_list(f_list, roots_ranges_list[i][0])), end='         ')
    print(count_sign_func_change(create_sign_list(f_list, roots_ranges_list[i][1])), end='         ')

print()
print("–ö–æ—Ä–Ω–∏ –ª–µ–∂–∞—Ç –Ω–∞: ", *roots_ranges_list)

